        public static string BuildQuery(string _class, string[] properties)
        {
            try
            {
                //Handle case for invalid _class parameter.
                if (!cimV2Classes.Contains(_class.ToLower())) { Console.WriteLine($"Invalid CIMV2 class provided, '{_class}'"); return "Error"; }
                if (properties.Length == 0) { Console.WriteLine("No properties were provided, exiting.."); return "Error"; }
                string wqlQuery = "SELECT";
                for (int i = 0; i < properties.Length; i++)
                {
                    if (i != properties.Length - 1)
                    {
                        wqlQuery += $" {properties[i]},";
                    }
                    else  // Ensure proper formatting as the last property requires a trailing space instead of a comma.
                    {
                        wqlQuery += $" {properties[i]} ";
                    }
                }
                wqlQuery += $"FROM {_class}";
                return wqlQuery;
            }
            catch (Exception ex)
            {
                Console.WriteLine("Unhandled Exception occured in WMIHelper.BuildQuery()");
                Console.WriteLine($"Type: {ex.GetType()}");
                Console.WriteLine($"Error: {ex.Message}");
                return "Error";
            }
        }


        [System.Runtime.Versioning.SupportedOSPlatform("windows")] // Supresses CA1416 
        public static Dictionary<string, string> GetInfo(string _class, string[] properties, string _type = "Not Provided")
        {
            PlatformID platformID = Environment.OSVersion.Platform;
            if (platformID == PlatformID.Win32NT)
            {
                try
                {


                    // Builds the wqlQuery dynamically, if BuildQuery() returns error the function prints said error then exits.
                    string wqlQuery;
                    if ((wqlQuery = BuildQuery(_class, properties)) == "Error") { Environment.Exit(1); }
                    SelectQuery query = new(wqlQuery); // Roslyn recommended this syntax over SelectQuery query = new SelectQury(wqlQuery);
                    ManagementScope scope = new();    // Roslyn recommended this syntax over ManagementScope scope = new Management();
                    Dictionary<string, string> results = []; // Roslyn recommended this syntax over new Dictionary<string, string>
                    scope.Connect();
                    using (ManagementObjectSearcher objectSearcher = new(scope, query))
                    {
                        // Used for debugging;
                        // Console.WriteLine("Querying WMI...");


                        int index = 0;
                        // objectSearcher.Get() returns a ManagementObjectCollection
                        foreach (ManagementObject managementObject in objectSearcher.Get().Cast<ManagementObject>())
                        {
                            index++;
                            // Memory safety is key to peak performance!
                            using (managementObject)
                            {
                                string returnValue;
                                foreach (string propertyName in properties)
                                {
                                    switch (_type.ToLower())
                                    {
                                        case "indexed":
                                            returnValue = managementObject[propertyName]?.ToString() ?? "Not Found";
                                            results.Add($"{propertyName}_{index}", returnValue);
                                            break;

                                        //case "cpu":
                                        //    returnValue = managementObject[propertyName]?.ToString() ?? "Not Found";
                                        //    if (propertyName.Contains("DeviceID")) { results.Add($"{propertyName}_{index}", returnValue); } // Only DeviceID will be added with an index
                                        //    else { results.Add($"{propertyName}", returnValue); }
                                        //    break;

                                        default:
                                            returnValue = managementObject[propertyName]?.ToString() ?? "Not Found";
                                            results.Add($"{propertyName}", returnValue);
                                            break;
                                    }
                                }
                                // List all properties
                                // Console.WriteLine("\n------All Properties-----");
                                // foreach (PropertyData prop in mo.Properties)
                                // {
                                //     Console.WriteLine($"{prop.Name}: {prop.Value}");
                                // }
                            }
                        }
                        return results;
                    }
                }
                catch (ManagementException ex)
                {
                    // Handle WMI errors
                    Console.WriteLine($"Error Code: {ex.ErrorCode}");
                    Console.WriteLine($"Error: {ex.Message}");
                    Environment.Exit(1);
                }
                catch (System.Runtime.InteropServices.COMException ex)
                {
                    // Handle low-level errors
                    Console.WriteLine($"Error Code: {ex.ErrorCode:X}"); // Displays error code as hex for easy googling 
                    Console.WriteLine($"Error: {ex.Message}");
                    Environment.Exit(1);
                }
                catch (Exception ex)
                {
                    // Handle other potential errors
                    Console.WriteLine($"General Error: {ex.Message}");
                    Environment.Exit(1);

                }
            }
            Console.ReadKey();

            return []; // Roslyn recommended this over the return statement below
            // return new Dictionary<string, string>(); // Empty dictionary will be handled using variable.Count
        }
